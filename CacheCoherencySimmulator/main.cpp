std::vector<std::bitset<13>> addressRequests;
void initAddressRequests();
int main()
{
    int cacheHit = 0;
    initAddressRequests();
    MemoryBlock memoryBlock;
    memoryBlock.initialiseMemoryBlock();
    int sizeOfConnectedMemoryInBytes = 5120;
    
    Cache myCache(256, 32, sizeOfConnectedMemoryInBytes);
    myCache.connectToMemory(&memoryBlock);

    for(int itr = 0; itr < addressRequests.size(); ++itr)
    {
        myCache.locateByDirectMapping(addressRequests[itr], cacheHit);
    }
    
    std::cout<<"Cache hit rate for Direct mapping, with Cache size 256B, Cache line size 32B : " << (float)cacheHit/1000<<std::endl;
    std::cout<<"Compulsory miss rate for Direct mapping, with Memory size 5120B, Cache line size 32B : " << (float)(5120/32)/1000<<std::endl;
    cacheHit = 0;

    myCache.reset(512, 32, sizeOfConnectedMemoryInBytes);

    for(int itr = 0; itr < addressRequests.size(); ++itr)
    {
       myCache.locateByDirectMapping(addressRequests[itr], cacheHit);
    }

    std::cout<<"Cache hit rate for Direct mapping, with Cache size 512B, Cache line size 32B : " << (float)cacheHit/1000<<std::endl;
    
    cacheHit = 0;

    myCache.reset(256, 64, sizeOfConnectedMemoryInBytes);

    for(int itr = 0; itr < addressRequests.size(); ++itr)
    {
        myCache.locateByDirectMapping(addressRequests[itr], cacheHit);
    }
    std::cout<<"Cache hit rate for Direct mapping, with Cache size 256B, Cache line size 64B : " << (float)cacheHit/1000<<std::endl;
    std::cout<<"Compulsory miss rate for Direct mapping, with Memory size 5120B, Cache line size 64B : " << (float)(5120/64)/1000<<std::endl;
    cacheHit = 0;

    myCache.reset(256, 32, sizeOfConnectedMemoryInBytes);

    for(int itr = 0; itr < addressRequests.size(); ++itr)
    {
        myCache.locateBySetAssociativeMapping(4, addressRequests[itr], cacheHit);
    }
    std::cout<<"Cache hit rate for 4-way set associative mapping, with Cache size 256B, Cache line size 32B : " << (float)cacheHit/1000<<std::endl;
    cacheHit = 0;
    myCache.reset();
    return 0;
}

